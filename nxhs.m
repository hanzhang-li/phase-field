% 本程序用于求解二维等参单元的形函数n，并且求解形函数对局部坐标的导数ndj
function [n,ndj]=nxhs(x1,y1)
% 求解形函数
n(1)=0.25*(1-x1)*(1-y1);
n(2)=0.25*(1+x1)*(1-y1);
n(3)=0.25*(1+x1)*(1+y1);
n(4)=0.25*(1-x1)*(1+y1);
% 求解形函数导数
ndj(1,1)=-0.25*(1-y1);
ndj(1,2)=0.25*(1-y1);
ndj(1,3)=0.25*(1+y1);
ndj(1,4)=-0.25*(1+y1);
ndj(2,1)=-0.25*(1-x1);
ndj(2,2)=-0.25*(1+x1);
ndj(2,3)=0.25*(1+x1);
ndj(2,4)=0.25*(1-x1);